#Assignment Operators (=, +=, -=, *=, /=, %=, **=, //=)

num = 5
print (num)

#-------------------------------------------------------------------------------------------------------------------------------------------------
#PLUS
num = num + 5
print (num)
#OR
num += 5
print (num)
#-------------------------------------------------------------------------------------------------------------------------------------------------
#DIF
num = num - 5
print (num)
#OR
num -= 4
print (num)
#-------------------------------------------------------------------------------------------------------------------------------------------------
#MUL
num = num * 4
print (num)
#OR
num *= 4
print (num)
#-------------------------------------------------------------------------------------------------------------------------------------------------
#DIV
num = num / 4
print (num)
#OR
num /= 4
print (num)
#-------------------------------------------------------------------------------------------------------------------------------------------------
#EXPO
num = num ** 4
print (num)
#OR
num **= 4
print (num)
#-------------------------------------------------------------------------------------------------------------------------------------------------
#MOD
num = num % 4
print (num)
#OR
num %= 4
print (num)