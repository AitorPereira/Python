#Comparation Operators (==, !=, >, <, >=, <=)

num1 = 5
num2 = 10

#Equal == 
if num1 == num2:
    print ("True")
else:
    print ("False")

string1 = "hello"
string2 = "hello"
if string1 == string2:
    print ("True")
else:
    print ("False")

#Dif != 
if num1 != num2:
    print ("True")
else:
    print ("False")

string1 = "hello"
string2 = "hello"
if string1 != string2:
    print ("True")
else:
    print ("False")

#Comparation > or < 
if num1 > num2:
    print ("True")
else:
    print ("False")

string1 = "hello"
string2 = "hell"
if string1 > string2:
    print ("True")
else:
    print ("False")

#Comparation >= or <= 
if num1 >= num2:
    print ("True")
else:
    print ("False")

string1 = "hello"
string2 = "hello"
if string1 >= string2:
    print ("True")
else:
    print ("False")